package lk.ijse.fieldguardianbackend.entity.enums;
/**
 * This enum class represents the status of a vehicle.
 * The status of a vehicle can be one of the following:
 * 1. AVAILABLE
 * 2. IN_USE
 * 3. OUT_OF_SERVICE
 * 4. REMOVED
 */
public enum VehicleStatus {
    AVAILABLE,
    IN_USE,
    OUT_OF_SERVICE,
    REMOVED
}