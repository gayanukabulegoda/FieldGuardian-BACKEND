package lk.ijse.fieldguardianbackend.entity.enums;
/**
 * This enum class represents the status of equipment.
 * The status of an equipment can be one of the following:
 * 1. AVAILABLE
 * 2. IN_USE
 * 3. UNDER_MAINTENANCE
 * 4. OUT_OF_SERVICE
 */
public enum EquipmentStatus {
    AVAILABLE,
    IN_USE,
    UNDER_MAINTENANCE,
    OUT_OF_SERVICE
}